    public void setExamLinkDetails(String examId) {
        List<Exam> resultLinkList = examSummaryService.getExamDetailsStream(examId);
        resultLinkList.forEach((result) -> {
            lblTotalDuration.setText(result.getExamDuration());
        });
    }
